package com.ynb.lesons_5;

/**
 * char => int   || int => char
 * test get value(int) from Letters(char) \ Letters to value
 */

public class LesExam_5 {
    public static void main(String[] args) {
        // char to int
        char symbol = 'A';
        System.out.println((int) symbol);
        // int to char
        int value = 65;
        char b = (char) value;
        System.out.println(b);

    }
}
